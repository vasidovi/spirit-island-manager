function PowerCard(set, type, name, cost, speed, range, target, elements, artist, description) {
    this.set = set;
    this.type = type;
    this.name = name;
    this.cost = cost;
    this.speed = speed;
    this.range = range;
    this.target = target;
    this.elements = elements;
    this.artist = artist;
    this.description = description;
}

function Ranges(source, range) {
    this.source = source;
    this.range = range;

}

